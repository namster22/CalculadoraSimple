public class metodos {
    
    public static int suma (int x, int y, int z){
    int suma=0;
    suma=x+y+z;
    return suma;
    }
    
     public static int resta (int x, int y, int z){
    int resta=0;
    resta=x-y-z;
    return resta;
    }
     
     public static int multiplicacion (int x, int y, int z){
    int multi=0;
    multi=x*y;
    return multi;
    }
     
     public static int division (int x, int y, int z){
    int div=0;
    div=x/y;
    return div;
    }
    
}
